#| tbl-cap: "Comparing two estimators of the average of random draws as the number of draws increases"

set.seed(853)

tibble(
    num_draws = c(
        rep(10, times = 10),
        rep(100, times = 100),
        rep(1000, times = 1000),
        rep(10000, times = 10000)
    ),
    draw = rnorm(
        n = length(num_draws),
        mean = 0,
        sd = 1
    )
) |>
    summarise(
        estimator_one = sum(draw) / unique(num_draws),
        estimator_two = sort(draw)[round(unique(num_draws) / 2, 0)],
        .by = num_draws
    ) |>
    kable(
        col.names = c("Number of draws", "Estimator one", "Estimator two"),
        digits = 2,
        format.args = list(big.mark = ",")
    )
